def load_users():
    if DATA_FILE.exists():
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}

def save_users(users):
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(users, f, ensure_ascii=False, indent=2)

def get_user(users, uid):
    uid = str(uid)
    if uid not in users:
        users[uid] = {
            "name": "",
            "lang": "ru",
            "stats": {s: {"correct": 0, "total": 0} for s in SUBJECTS},
            "asked": {s: [] for s in SUBJECTS},
            "broadcast": True,
            "plan": "free",
            "plan_until": None,
            "questions_today": 0,
            "last_question_date": "",
            "test_today": 0,
            "last_test_date": "",
            "test_mode": False,
            "test_subject": None,
            "pending_payment": None,
        }
    if "lang" not in users[uid]:
        users[uid]["lang"] = "ru"
    return users[uid]

def get_lang(user):
    return user.get("lang", "ru")

def is_premium(user):
    if user.get("plan") in ("standard", "premium", "premium_test"):
        until = user.get("plan_until")
        if until and datetime.now() < datetime.fromisoformat(until):
            return True
        user["plan"] = "free"
    return False

def has_test_access(user):
    return is_premium(user) and user.get("plan") == "premium_test"

def reset_daily_if_needed(user):
    today = datetime.now(ALMATY_TZ).strftime("%Y-%m-%d")
    if user.get("last_question_date") != today:
        user["questions_today"] = 0
        user["last_question_date"] = today
    if user.get("last_test_date") != today:
        user["test_today"] = 0
        user["last_test_date"] = today

def can_get_question(user):
    reset_daily_if_needed(user)
    plan = user.get("plan", "free") if is_premium(user) else "free"
    limit = PLAN_NOTIFY_LIMIT.get(plan, 1)
    return user["questions_today"] < limit

def can_get_test_question(user):
    reset_daily_if_needed(user)
    return user["test_today"] < TEST_DAILY_LIMIT

def pick_question(user, canonical_subject, lang):
    pool_ru = QUESTIONS_RU[canonical_subject]
    pool_lang = QUESTIONS_BY_LANG[lang][subject_to_display(canonical_subject, lang)] if lang == "kk" else pool_ru
    asked = user["asked"].get(canonical_subject, [])
    available = [i for i in range(len(pool_ru)) if i not in asked]
    if not available:
        user["asked"][canonical_subject] = []
        available = list(range(len(pool_ru)))
    idx = random.choice(available)
    user["asked"][canonical_subject].append(idx)
    return idx, pool_lang[idx]

def get_stats_text(user):
    lang = get_lang(user)
    plan_key = user.get("plan", "free") if is_premium(user) else "free"
    plan_label = t(lang, "free_label") if plan_key == "free" else f"⭐️ {PLAN_NAMES[plan_key]}"
    until = ""
    if is_premium(user) and user.get("plan_until"):
        exp = datetime.fromisoformat(user["plan_until"])
        until = f" (до {exp.strftime('%d.%m.%Y')})" if lang == "ru" else f" ({exp.strftime('%d.%m.%Y')} дейін)"
    lines = [t(lang, "stats_title", plan=plan_label, until=until)]
    total_c, total_t = 0, 0
    for s in SUBJECTS:
        st = user["stats"].get(s, {"correct": 0, "total": 0})
        c, tt = st["correct"], st["total"]
        total_c += c; total_t += tt
        pct = round(c/tt*100) if tt > 0 else 0
        bar = "🟢" * (pct // 20) + "⬜" * (5 - pct // 20)
        display_name = subject_to_display(s, lang)
        lines.append(f"{bar} *{display_name}*: {c}/{tt} ({pct}%)")
    if total_t > 0:
        overall = round(total_c/total_t*100)
        lines.append(t(lang, "stats_total", c=total_c, t=total_t, pct=overall))
        if overall >= 80:
            lines.append(t(lang, "rating_great"))
        elif overall >= 60:
            lines.append(t(lang, "rating_good"))
        else:
            lines.append(t(lang, "rating_more"))
    else:
        lines.append(t(lang, "stats_no_answers"))
    if has_test_access(user):
        reset_daily_if_needed(user)
        lines.append(t(lang, "stats_test_today", today=user["test_today"], limit=TEST_DAILY_LIMIT))
    return "\n".join(lines)

